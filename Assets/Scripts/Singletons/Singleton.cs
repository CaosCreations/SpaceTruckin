using UnityEngine;

public abstract class Singleton<T> : MonoBehaviour
{
    public static Singleton<T> Instance;

    private void Awake()
    {
        if (Instance == null)
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
            return;
        }
    }
}
